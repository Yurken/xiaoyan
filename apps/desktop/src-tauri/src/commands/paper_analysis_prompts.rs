pub(crate) fn build_all_figure_context(figures: &[PaperFigureContext]) -> String {
    let all_figures = figures
        .iter()
        .filter(|f| !f.kind.eq_ignore_ascii_case("table"))
        .take(12)
        .map(|figure| {
            let label = figure.reference_label();
            if let Some(caption) = figure.caption.as_deref() {
                format!("  • {label}: {caption}")
            } else {
                format!("  • {label}")
            }
        })
        .collect::<Vec<_>>();

    if all_figures.is_empty() {
        return String::new();
    }

    format!(
        "【可用图表上下文（按引用需要自然使用）】\n{}\n
在解读中，你可以在以下场景自然引用这些图表：\n- 说明问题背景或研究动机时，引用概念图、问题示意图（如 Figure 1）。\n- 解释方法或框架时，引用架构图、流程图、模块图（如 Figure 2-4）。\n- 分析实验结果时，引用对比图、消融图、可视化图（如 Figure 5-8）。\n- 引用格式统一为纯文本 `Figure X` 或 `Table X`，不要写 Markdown 图片语法，不要编造不存在的编号。\n\n",
        all_figures.join("\n")
    )
}

const PAPER_NOTE_PROMPT: &str = r#"请基于以下论文信息生成一条结构化的知识笔记，仅返回严格合法的 JSON，不输出任何其他内容。

核心原则：
- 笔记面向「研究者后续回顾、写作引用与灵感触发」，不是摘要复读，而是提炼真正值得记住、可复用、可行动的内容。
- 内容使用中文；专有名词、公式、变量名、代码可保留英文。
- 公式使用 LaTeX：行内 $...$，独立 $$...$$。
- 使用 Markdown 组织内容：标题用 ## / ###，要点用无序列表，关键术语用 **加粗**。
- 不要编造论文没有的信息；若某部分确实没有相关内容，直接省略对应章节，不要写「暂无」。
- 不要输出图片占位符或编造 Figure/Table 编号。若论文解读中提到了 Figure/Table 编号，可以保留为纯文本引用。
- tags 应为 3-8 个中文或英文关键词/标签，便于后续检索。

输入信息：

论文标题：{title}
作者：{authors}
年份：{year}
会议/期刊：{venue}
DOI：{doi}

摘要：
{abstract}

论文解读：
{analysis}

复现/验证指南：
{reproduction_guide}

用户已有备注：
{notes}

输出要求：
- title：直接使用论文标题「{title}」，不要添加“论文笔记：”“笔记 - ”等前缀或改写标题。
- content：结构化的 Markdown 笔记正文，按论文实际情况取舍章节，不要机械套用所有栏目。建议包含以下章节：

  - ## 一句话概括
    - 用 1-2 句话说明这篇论文做了什么、核心贡献是什么。不要复述标题，要给出实质性判断。

  - ## 关键术语速查（Terminology Ledger）
    - 只列出 3-6 个真正重要、易混淆或后续写作中会反复引用的术语。不要罗列所有术语。
    - 使用 Markdown 表格：| 英文术语 | 中文释义 | 为什么重要 |

  - ## 核心问题与 Gap
    - 作者试图解决什么真实问题？现有工作在哪里不足？这个 Gap 是否成立？
    - 来源锚点：标注信息来自「摘要」「解读：问题与语境」或「解读：方法/框架」。

  - ## 关键方法/技术 Insight
    - 核心思路一句话概括 + 为什么这个设计有效（Technical Insight）。
    - 重点提炼：作者解决了什么前人没解决的困难？方法设计中的“巧妙之处”是什么？
    - 如果方法中有可以直接借鉴的公式、框架或流程，单独用「可借鉴的表述」子栏目标出。
    - 来源锚点：标注信息来自「解读：方法/框架」或「解读：证据与结果」。

  - ## 主要发现与证据强度
    - 最重要的结果是什么？证据是否充分？有没有过度推销？
    - 对实验论文，评估：基线是否公平？消融是否关键？指标是否合理？
    - 来源锚点：标注信息来自「解读：结果/发现」或「解读：证据与验证」。

  - ## 创新点与可迁移性
    - 这篇论文的真正贡献是什么？是技术、理论、框架还是洞察？
    - 这个创新能否迁移到我的研究方向？有哪些适配门槛？

  - ## 局限与可改进的缺口
    - 论文的实质性局限是什么？有哪些作者没做但值得做的后续方向？
    - 特别提炼：「我可以做的改进」——基于论文的不足，指出 1-2 个具体、可行的研究缺口。

  - ## 可借鉴的写作素材
    - 摘录 2-4 个原文中表达精良、可直接引用或仿写的句子/公式/框架描述。注明来源（如：摘要、方法第一段）。
    - 这部分是“素材库”，帮助用户后续写论文时快速引用。

  - ## 与我工作的关系（给出具体引导，不要只写提示）
    - 基于论文的 Motivation、Method、Limitation，给出以下思考框架：
      - 这篇论文解决的问题与我的工作是否相关？（相关 / 部分相关 / 不相关，说明理由）
      - 论文中的方法/技术能否直接用于我的研究？如果能，需要哪些适配？
      - 论文的局限中，是否有我可以填补的 Gap？
      - 读完这篇论文，我下一步应该做什么？（例如：复现核心实验、对比我的方法、引用其理论框架、改进其消融设置等）
    - 如果用户已有备注（见上方输入），优先融合用户备注，不要与用户已有观点冲突。

  - ## 后续行动清单（Action Items）
    - 给出 2-4 个具体、可执行的行动项。例如：
      - 复现：需要多长时间？核心代码是否开源？
      - 对比：与我当前方法的优劣对比维度是什么？
      - 改进：基于论文的某个假设放松，可以做哪些扩展？
      - 引用：这篇论文适合放在我写作的哪个位置（相关工作、方法基础、对比实验）？
      - 阅读：这篇论文引用了哪些我必须补充阅读的经典文献？

- tags：字符串数组，优先从论文关键词和核心方法中提炼。

返回格式（严格 JSON，不得有多余字符）：
{"title": "...", "content": "...", "tags": ["...", "..."]"#;

pub(crate) fn build_paper_note_prompt(
    title: &str,
    authors: &str,
    year: &str,
    venue: &str,
    doi: &str,
    abstract_text: &str,
    analysis: &str,
    reproduction_guide: &str,
    notes: &str,
) -> String {
    PAPER_NOTE_PROMPT
        .replace("{title}", title)
        .replace("{authors}", authors)
        .replace("{year}", year)
        .replace("{venue}", venue)
        .replace("{doi}", doi)
        .replace("{abstract}", abstract_text)
        .replace("{analysis}", analysis)
        .replace("{reproduction_guide}", reproduction_guide)
        .replace("{notes}", notes)
}

