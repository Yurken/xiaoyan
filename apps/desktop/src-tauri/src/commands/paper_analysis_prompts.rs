const POLISH_TEXT_PROMPT: &str = r#"请对以下学术文本进行 Nature 风格的润色/改写，仅返回严格合法的 JSON，不要输出任何其他内容。

润色原则（基于 nature-polishing）：
- 语言服务于论证：不要只润色句子而留下破碎的逻辑。
- 读者优先：先说明相关性，再讲新颖性，再建立信任，再谈可复用性，最后讲意义。
- 不编造数据、引用、机制或新颖性声明。
- 如果原文是中文或结构粗糙，先重建逻辑，再润色表达。
- 建立术语表并保持术语、缩写、单位、符号在各处一致，不要随意换同义词。
- 避免过多使用 em dash，优先用逗号、括号或句号。
- 先诊断主要问题（论文类型逻辑错误、缺少 gap、claim 无证据、证据无 claim、缺少边界、结果与讨论混用、标题/摘要信号弱、术语不一致、仅句子层面的冗长），再按以下优先级修复：paper type -> section job -> paragraph logic -> claim/evidence/boundary -> sentence polish。
- 如果某段结构问题无法在不编造内容的情况下修复，在 warnings 中明确指出，而不是用漂亮语言掩盖。

输入信息：
- section（目标章节）：{section}
- direction（润色方向）：{direction}
- text（待润色文本）：
{text}

输出要求：
- polished：润色后的文本。保持原意和证据链，使用学术英文或中文（根据 direction 决定）。不要输出代码块包裹。
- revision_notes：3-5 条修改说明，列出主要的结构和语言调整。
- warnings：如果原文存在无法在不编造内容前提下修复的结构问题，列出警告；否则返回空数组。

返回格式（严格 JSON，不得有多余字符）：
{"polished": "...", "revision_notes": ["...", "..."], "warnings": ["...", "..."]"#;

